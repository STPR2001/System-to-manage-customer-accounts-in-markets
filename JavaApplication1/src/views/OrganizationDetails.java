    public void loadOrganizationDetails(String id) {
        Organization organization = this.IOrganization.getOrganizationById(id);
        ArrayList<AccountUser> clientsInfo = this.IOrganization.getAccountAndUserDetails(id);

        this.jLabel1.setText(organization.getName());
        DefaultTableModel tableModel = (DefaultTableModel) this.jTable1.getModel();
        
        for(AccountUser client : clientsInfo) {
            Object[] newRow = {
                client.getUser().getName(),
                client.getUser().getEmail(),
                "Hace 2 horas",
                client.getAccount().getTotal()
            };
            tableModel.addRow(newRow);
        }
    }
    
